package knivelarea;

/**
 *
 * @author usuario
 */
public class KnivelArea {
    private int idNivelArea;
    private String descripcion;
    private String orden;
    private String observaciones;
    private int idEstatus;
    private boolean nivelAreaSeleccionado;

    public KnivelArea() {
    }

    public KnivelArea(int idNivelArea, String descripcion, String orden, String observaciones, int idEstatus, boolean nivelAreaSeleccionado) {
        this.idNivelArea = idNivelArea;
        this.descripcion = descripcion;
        this.orden = orden;
        this.observaciones = observaciones;
        this.idEstatus = idEstatus;
        this.nivelAreaSeleccionado = nivelAreaSeleccionado;
    }

    public int getIdNivelArea() {
        return idNivelArea;
    }

    public void setIdNivelArea(int idNivelArea) {
        this.idNivelArea = idNivelArea;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = null != descripcion ? descripcion.toUpperCase() : null;
    }

    public String getOrden() {
        return orden;
    }

    public void setOrden(String orden) {
        this.orden = orden;
    }

    public String getObservaciones() {
        return observaciones;
    }

    public void setObservaciones(String observaciones) {
        this.observaciones = observaciones;
    }

    public int getIdEstatus() {
        return idEstatus;
    }

    public void setIdEstatus(int idEstatus) {
        this.idEstatus = idEstatus;
    }

    public boolean isNivelAreaSeleccionado() {
        return nivelAreaSeleccionado;
    }

    public void setNivelAreaSeleccionado(boolean nivelAreaSeleccionado) {
        this.nivelAreaSeleccionado = nivelAreaSeleccionado;
    }

    

}
