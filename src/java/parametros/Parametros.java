package parametros;

import java.io.Serializable;
import java.sql.Blob;
import org.primefaces.model.StreamedContent;

/**
 *
 * @author usuario
 */
public class Parametros implements Serializable {
    private int idParametro;
    private int tiempoMaximoModif;
    private String mostrarInactivos;
    private Blob imagen;
    private byte[] imagenByte;
    private StreamedContent file;
    private Integer noPersonalBm;
    private String nomPersonal;
    private String funcion;
    private int idEvento;
    private int numDependencia;
    private String dependencia;
    public Parametros() {
    }

    public int getIdParametro() {
        return idParametro;
    }

    public void setIdParametro(int idParametro) {
        this.idParametro = idParametro;
    }

    public int getTiempoMaximoModif() {
        return tiempoMaximoModif;
    }

    public void setTiempoMaximoModif(int tiempoMaximoModif) {
        this.tiempoMaximoModif = tiempoMaximoModif;
    }

    public String getMostrarInactivos() {
        return mostrarInactivos;
    }

    public void setMostrarInactivos(String mostrarInactivos) {
        this.mostrarInactivos = mostrarInactivos;
    }

    public Blob getImagen() {
        return imagen;
    }

    public void setImagen(Blob imagen) {
        this.imagen = imagen;
    }

    public byte[] getImagenByte() {
        return imagenByte;
    }

    public void setImagenByte(byte[] imagenByte) {
        this.imagenByte = imagenByte;
    }

    public StreamedContent getFile() {
        return file;
    }

    public void setFile(StreamedContent file) {
        this.file = file;
    }
    
    public Integer getNoPersonalBm() {
        return noPersonalBm;
    }

    public void setNoPersonalBm(Integer noPersonalBm) {
        this.noPersonalBm = noPersonalBm;
    }

    public String getFuncion() {
        return funcion;
    }

    public void setFuncion(String funcion) {
        this.funcion = funcion;
    }

    public int getIdEvento() {
        return idEvento;
    }

    public void setIdEvento(int idEvento) {
        this.idEvento = idEvento;
    }

    public int getNumDependencia() {
        return numDependencia;
    }

    public void setNumDependencia(int numDependencia) {
        this.numDependencia = numDependencia;
    }

    public String getDependencia() {
        return dependencia;
    }

    public void setDependencia(String dependencia) {
        this.dependencia = dependencia;
    }

    public String getNomPersonal() {
        return nomPersonal;
    }

    public void setNomPersonal(String nomPersonal) {
        this.nomPersonal = nomPersonal;
    }

    
}