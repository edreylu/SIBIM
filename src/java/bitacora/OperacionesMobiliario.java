package bitacora;

/**
 *
 * @author usuario
 */
public class OperacionesMobiliario {
private int numeroCarga;
private Integer noPersonalBm;
private String nomPersonalBm;
private int idDepartamento;
private String nomDep;
private int noUsuarioOperacion;

    public OperacionesMobiliario() {
    }

    public OperacionesMobiliario(int numeroCarga, Integer noPersonalBm, String nomPersonalBm, int idDepartamento, String nomDep, int noUsuarioOperacion) {
        this.numeroCarga = numeroCarga;
        this.noPersonalBm = noPersonalBm;
        this.nomPersonalBm = nomPersonalBm;
        this.idDepartamento = idDepartamento;
        this.nomDep = nomDep;
        this.noUsuarioOperacion = noUsuarioOperacion;
    }

    public String getNomDep() {
        return nomDep;
    }

    public void setNomDep(String nomDep) {
        this.nomDep = nomDep;
    }

    public int getNumeroCarga() {
        return numeroCarga;
    }

    public void setNumeroCarga(int numeroCarga) {
        this.numeroCarga = numeroCarga;
    }

    public Integer getNoPersonalBm() {
        return noPersonalBm;
    }

    public void setNoPersonalBm(Integer noPersonalBm) {
        this.noPersonalBm = noPersonalBm;
    }

    public int getIdDepartamento() {
        return idDepartamento;
    }

    public void setIdDepartamento(int idDepartamento) {
        this.idDepartamento = idDepartamento;
    }

    public int getNoUsuarioOperacion() {
        return noUsuarioOperacion;
    }

    public void setNoUsuarioOperacion(int noUsuarioOperacion) {
        this.noUsuarioOperacion = noUsuarioOperacion;
    }

    public String getNomPersonalBm() {
        return nomPersonalBm;
    }

    public void setNomPersonalBm(String nomPersonalBm) {
        this.nomPersonalBm = nomPersonalBm;
    }

}
