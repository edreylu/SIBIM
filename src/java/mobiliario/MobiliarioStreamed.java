package mobiliario;

import java.awt.Image;
import java.sql.Blob;
import org.primefaces.component.graphicimage.GraphicImage;
import org.primefaces.model.StreamedContent;

/**
 *
 * @author usuario
 */
public class MobiliarioStreamed {
private int noImagen;
private Blob imagen;
private int idBien;
private String nombreArchivo;
private String tipoArchivo;
private byte[] imagenByte;
private Image img;
private StreamedContent file;
    public MobiliarioStreamed() {
    }

    public MobiliarioStreamed(int noImagen, Blob imagen, int idBien, String nombreArchivo, String tipoArchivo, byte[] imagenByte, Image img, StreamedContent file) {
        this.noImagen = noImagen;
        this.imagen = imagen;
        this.idBien = idBien;
        this.nombreArchivo = nombreArchivo;
        this.tipoArchivo = tipoArchivo;
        this.imagenByte = imagenByte;
        this.img = img;
        this.file = file;
    }
    public int getNoImagen() {
        return noImagen;
    }

    public void setNoImagen(int noImagen) {
        this.noImagen = noImagen;
    }

    public Blob getImagen() {
        return imagen;
    }

    public void setImagen(Blob imagen) {
        this.imagen = imagen;
    }

    public int getIdBien() {
        return idBien;
    }

    public void setIdBien(int idBien) {
        this.idBien = idBien;
    }

    public String getNombreArchivo() {
        return nombreArchivo;
    }

    public void setNombreArchivo(String nombreArchivo) {
        this.nombreArchivo = nombreArchivo;
    }

    public String getTipoArchivo() {
        return tipoArchivo;
    }

    public void setTipoArchivo(String tipoArchivo) {
        this.tipoArchivo = tipoArchivo;
    }

    public byte[] getImagenByte() {
        return imagenByte;
    }

    public void setImagenByte(byte[] imagenByte) {
        this.imagenByte = imagenByte;
    }

    public Image getImg() {
        return img;
    }

    public void setImg(Image img) {
        this.img = img;
    }

    public StreamedContent getFile() {
        return file;
    }

    public void setFile(StreamedContent file) {
        this.file = file;
    }

}
